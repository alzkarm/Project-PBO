    private void simpan_supActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpan_supActionPerformed
       String kodeSup = kode_supplier.getText();
       String name = nama_supplier.getText();
        String address = alamat.getText();
        String phone = tlpn.getText();
        String emailSup = email.getText();
    
        // Periksa apakah kode supplier sudah ada di database
        if (supplierExists(kodeSup)) {
        JOptionPane.showMessageDialog(null, "Supplier dengan kode tersebut sudah ada");
        return;
        }
        
        if (kodeSup.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Kode Supplier belum diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; // Keluar dari method jika kode barang belum diisi
        }
        
        if (address.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Alamat Supplier belum diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; // Keluar dari method jika kode barang belum diisi
        }
        
        if (emailSup.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Email Supplier belum diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; // Keluar dari method jika kode barang belum diisi
        }
    
        Supplier supplier = new Supplier(kodeSup, name,address, phone, emailSup);
        supplier.save();
        tampilData();

    }//GEN-LAST:event_simpan_supActionPerformed

    private boolean supplierExists(String kodeSup) {
    boolean exists = false;
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/projectpbo", "root", "");
         PreparedStatement ps = con.prepareStatement("SELECT COUNT(*) FROM supplier WHERE kode_sup = ?")) {

        ps.setString(1, kodeSup);
        ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            int count = rs.getInt(1);
            exists = count > 0;
        }

    } catch (SQLException e) {
        e.printStackTrace();
    }
    return exists;
    }
    
    private void ubah_supActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubah_supActionPerformed
     DefaultTableModel table = (DefaultTableModel) tableSup.getModel();
    int selectedRow = tableSup.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Pilih satu baris untuk di-update");
        return;
    }
    
     String kodeSupplierDariTabel = table.getValueAt(selectedRow, 0).toString();
    String kodeSupplierInput = kode_supplier.getText().trim();

    if (!kodeSupplierInput.equals(kodeSupplierDariTabel)) {
        JOptionPane.showMessageDialog(null, "Kode Supplier tidak bisa di ubah", "Warning", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Ambil nilai kode supplier dari baris yang dipilih
    String kodeSuplier = table.getValueAt(selectedRow, 0).toString();
    String namaSupplier = nama_supplier.getText().trim();
    String alamat = this.alamat.getText().trim();
    String noHp = tlpn.getText().trim();
    String email = this.email.getText().trim();

    try {
        // Load the MySQL JDBC driver
        Class.forName("com.mysql.cj.jdbc.Driver");
        // Database connection
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/projectpbo", "root", ""); 

        // Perbarui data supplier di database
        String sql = "UPDATE supplier SET nama_sup = ?, tlpn = ?, email = ?, alamat = ? WHERE kode_sup = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, namaSupplier);
        stmt.setString(2, noHp);
        stmt.setString(3, email);
        stmt.setString(4, alamat);
        stmt.setString(5, kodeSuplier); // Pastikan ini benar

        int rowsUpdated = stmt.executeUpdate();
        stmt.close();
        conn.close();

        if (rowsUpdated > 0) {
            // Update the row in the GUI table
            table.setValueAt(namaSupplier, selectedRow, 1);
            table.setValueAt(noHp, selectedRow, 2);
            table.setValueAt(email, selectedRow, 3);
            table.setValueAt(alamat, selectedRow, 4);

            JOptionPane.showMessageDialog(null, "Data berhasil di-update");
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan di database");
        }
    } catch (ClassNotFoundException ex) {
        JOptionPane.showMessageDialog(null, "Driver tidak ditemukan: " + ex.getMessage());
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_ubah_supActionPerformed

