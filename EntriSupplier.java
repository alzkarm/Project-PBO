  private void tampilData() {
        DefaultTableModel table = (DefaultTableModel) tableSup.getModel();
    table.setRowCount(0);
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/projectpbo", "root", "")) {
        String query = "SELECT kode_sup, nama_sup, tlpn, email, alamat FROM supplier";
        Statement statement = con.createStatement();
        ResultSet resultSet = statement.executeQuery(query);

        while (resultSet.next()) {
            String kodeSup = resultSet.getString("kode_sup");
            String name = resultSet.getString("nama_sup");
            String phone = resultSet.getString("tlpn");
            String email = resultSet.getString("email");
            String address = resultSet.getString("alamat");
            table.addRow(new Object[]{kodeSup, name, phone, email, address});
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    tableSup.setModel(table);
    }


    


